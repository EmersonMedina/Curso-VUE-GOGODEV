/// <reference types="cypress" />

declare global {
    namespace Cypress {
      interface Chainable {
        login(email: string, password: string): void;
      }
    }
  }

Cypress.Commands.add('login', (email: string, password: string) => {
    cy.visit('/login')
    cy.get('#email').type(email)
    cy.get('#password').type(password)
    cy.get('#login_btn').click()
})

